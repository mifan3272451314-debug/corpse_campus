    public static String resolveSchoolPath(ResourceLocation schoolId) {
        if (schoolId == null) {
            return null;
        }
        return schoolId.getPath();
    }

    public static boolean isKnownSchool(String path) {
        return path != null && SCHOOL_DISPLAY_NAME.containsKey(path);
    }

    public static ResourceLocation toResourceLocation(String path) {
        return switch (path) {
            case "xujing" -> ModSchools.XUJING_RESOURCE;
            case "rizhao" -> ModSchools.RIZHAO_RESOURCE;
            case "dongyue" -> ModSchools.DONGYUE_RESOURCE;
            case "yuzhe" -> ModSchools.YUZHE_RESOURCE;
            case "shengqi" -> ModSchools.SHENGQI_RESOURCE;
            default -> null;
        };
    }
